			<h2>Как грамотно писать статьи</h2>
			<p>Перед тем как писать статьи для сайтов, надо изучить материал, с которым предстоит работать. 
			Посмотреть сайты конкурентов по данной тематике. Выяснить, какие они используют ключевые 
			фразы и описания в метатеге Description. Хотя на сегодняшний день поисковые роботы почти 
			не обращают внимания на ключевые фразы. Им, впрочем, как и читателю, важно прежде всего 
			содержание статьей, которые вы пишете для сайтов.</p>

				<p>Всякая уважающая себя статья, которую вы хотите разместить на сайте, должна содержать:</p>

				 <ol>

				<li>Абзацы;</li>

				<li>Уникальные (то есть переделанные в фотошопе или просто обрезанные) 
				фотографии с alt и title, в которых есть ключевые фразы;</li>

				<li>Списки (как нумерованные, так и не нумерованные);</li>

				<li>Ссылки на главную страницу (не более двух), удачно вписанные в контекст статьи для сайта;</li>

				<li>На 1000 знаков (без учета пробелов)  – около 6 ключевых слов 
				(в первом и последнем абзацах их плотность выше, чем в центре). 
				Плотность ключевых слов в тексте статьи для сайта – 5 – 7%. 
				Если в статье 100 слов, то должно быть 5-7 ключевых слов. 
				Выделяйте ключевые слова тегами h1, h2 и т.д.;</li>

				<li>По меньшей мере три точных вхождения ключевых фраз;</li>

				<li>Обязательно дайте своим читателям в статье на сайте один особый, 
				редкий совет, который не может дать никто другой;</li>

				<li>Завершающий шаг - призыв к действию. Можно попросить высказать 
				свою точку зрения в комментариях к статье, сделать репост на другой сайт и т. д.</li>

				</ol> 

				<p>Желательно, чтобы статья содержала более 1000 знаков (а лучше 2500) и 
				была уникальной. Уникальность достигается двумя способами:</p>
				<ol>
				<li>Написать статью для сайта самому (или рерайт);</li>

				<li>Перевести иностранную статью по той же тематике.</li>
				</ol>
				 

				<p>Размещать статьи на сайтах оптимальнее всего через день 
				(чтобы робот не счел вас спамером). Также вполне подойдет 
				ежедневная публикация, в одно и то же время. Так вы "приучите" 
				робота регулярно заходить к вам на сайт. Статьи лучше выкладывать 
				не полностью, а по частям. Скажем, по 1000 знаков в день. 
				Такие кусочки для роботов как наживка. Легко глотаются.</p>

				<p>Продвигать статью лучше всего по одной-двум ключевым фразам 
				(по среднечастотным запросам – 1000 – 100 000). Это эффективнее. 
				Надо, чтобы в фразе были сразу и низко-, и высокочастотные запросы.</p>

				 

				<p>P.S. Последовательность ключевых слов при разрыве ключевой 
				фразы в тексте статьи должна быть правильной, то есть слова 
				должны следовать друг за другом поочередно, как в ключевой фразе. </p>
							
